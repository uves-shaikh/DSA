#include <bits/stdc++.h>
using namespace std;

int main()
{
    // Different data types in C++

    int a = 10;            // integer
    float b = 5.5;         // single precision floating point
    double c = 3.14159265; // double precision floating point
    char d = 'X';          // single character
    bool e = true;         // boolean (true/false)

    cout << "Integer: " << a << endl;
    cout << "Float: " << b << endl;
    cout << "Double: " << c << endl;
    cout << "Char: " << d << endl;
    cout << "Bool: " << e << endl;

    return 0;
}
