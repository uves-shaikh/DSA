#include <bits/stdc++.h>
using namespace std;

int main()
{
    // Example: Print first N natural numbers using for loop

    int n;
    cout << "Enter N: ";
    cin >> n;

    cout << "First " << n << " natural numbers: ";
    for (int i = 1; i <= n; i++)
    {
        cout << i << " ";
    }
    cout << endl;

    return 0;
}
